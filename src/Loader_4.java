public class Loader_4 {

    public static void main(String[] args) {
        int x = 15;

        if (x == 10) {
            System.out.print("x = 10");
        } else if (x == 20) {
            System.out.print("x = 20");
        } else {
            System.out.print("Подходящего условия нет!");
        }
    }
}
